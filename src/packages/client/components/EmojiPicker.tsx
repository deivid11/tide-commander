// Group emojis by category
const EMOJI_BY_CATEGORY = EMOJI_DATA.reduce((acc, item) => {
  if (!acc[item.category]) acc[item.category] = [];
  acc[item.category].push(item);
  return acc;
}, {} as Record<string, typeof EMOJI_DATA>);

// Category order
const CATEGORY_ORDER = [
  'Smileys', 'People', 'Gestures', 'Hearts', 'Animals', 'Nature', 'Weather',
  'Food', 'Tools', 'Tech', 'Transport', 'Objects', 'Symbols', 'Flags',
];

interface EmojiPickerProps {
  value: string;
  onChange: (emoji: string) => void;
}

export function EmojiPicker({ value, onChange }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false);
  const [search, setSearch] = useState('');
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    function handleClickOutside(event: MouseEvent) {
      if (containerRef.current && !containerRef.current.contains(event.target as Node)) {
        setIsOpen(false);
      }
    }
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  const filteredResults = useMemo(() => {
    const query = search.trim().toLowerCase();
    if (!query) {
      return CATEGORY_ORDER.map(category => ({
        category,
        emojis: EMOJI_BY_CATEGORY[category] || [],
      })).filter(group => group.emojis.length > 0);
    }
    const matches = EMOJI_DATA.filter(item =>
      item.keywords.some(keyword => keyword.includes(query)) || item.emoji === query
    );
    return matches.length ? [{ category: 'Results', emojis: matches }] : [];
  }, [search]);

  const handleSelect = (emoji: string) => {
    onChange(emoji);
    setIsOpen(false);
    setSearch('');
  };

  return (
    <div ref={containerRef} className="emoji-picker-container">
      <div className="emoji-picker-input-wrapper">
        <input
          type="text"
          className="form-input emoji-picker-input"
          value={value}
          onChange={(e) => onChange(e.target.value)}
          placeholder="ğŸš€"
          onClick={() => setIsOpen(true)}
          style={{ textAlign: 'center', fontSize: '18px', paddingRight: '32px' }}
        />
        <button
          type="button"
          className="emoji-picker-toggle"
          onClick={() => setIsOpen(!isOpen)}
          title="Browse emojis"
        >
          â–¼
        </button>
      </div>
      {isOpen && (
        <div className="emoji-picker-dropdown">
          <div className="emoji-picker-search">
            <input
              type="text"
              className="form-input"
              placeholder="Search emojis..."
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              autoFocus
            />
          </div>
          <div className="emoji-picker-grid-container">
            {filteredResults.length === 0 ? (
              <div className="emoji-picker-empty">No emojis found</div>
            ) : (
              filteredResults.map(({ category, emojis }) => (
                <div key={category} className="emoji-picker-category">
                  <div className="emoji-picker-category-label">{category}</div>
                  <div className="emoji-picker-grid">
                    {emojis.map((item, idx) => (
                      <button
                        key={`${item.emoji}-${idx}`}
                        type="button"
                        className={`emoji-picker-item ${value === item.emoji ? 'selected' : ''}`}
                        onClick={() => handleSelect(item.emoji)}
                        title={item.keywords.slice(0, 3).join(', ')}
                      >
                        {item.emoji}
                      </button>
                    ))}
                  </div>
                </div>
              ))
            )}
          </div>
        </div>
      )}
    </div>
  );
}
